num = int(input("Enter Grade: "))

if(num > 100):
    print("Please Input Grade 0 - 100")
elif(num >= 90):
    print("A+")
elif(num >= 80):
    print("A")
elif(num >= 75):
    print("B+")
elif(num >= 70):
    print("B")
elif(num >= 65):
    print("C+")
elif(num >= 60):
    print("C")
elif(num >= 55):
    print("D+")
elif(num >= 50):
    print("D")
elif(num >= 0):
    print("F")
else:
    print("ERROR")

    
    
